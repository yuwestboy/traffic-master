package com.everpower.traffic_api_entity;

public class RoleEntity {

    private long rid;
    private String rname;
    private String rtype;
    private String desc;

    public long getRid() { return rid; }

    public void setRid(long rid) { this.rid = rid; }

    public String getRname() { return rname; }

    public void setRname(long rname) { this.rname = rname; }

    public String getRtype() { return rtype; }

    public void setRtype(long rtype) { this.rtype = rtype; }

    public String getDesc() { return desc; }

    public void setDesc(long desc) { this.desc = desc; }

}
